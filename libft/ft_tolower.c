#include <stdio.h>

int		ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		return (c + 32);
	}
	else
	{
		return (c);
	}
}

int main()
{
	printf("%c", ft_tolower('J'));
	return (0);
}
